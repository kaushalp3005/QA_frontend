// ── Top section: header field grid (Sr. No., dates, supplier, etc.) ───
export type HeaderFieldType = 'text' | 'date' | 'yesno' | 'number'

export interface HeaderFieldDef {
  key: string
  label: string
  type: HeaderFieldType
  /**
   * Where the value originates:
   *   - 'auto'   → generated by the system (e.g. Sr. No. from a sequence)
   *   - 'grn'    → auto-filled from the GRN row passed via sessionStorage
   *   - undefined → manually entered by the inspector
   */
  source?: 'auto' | 'grn'
  /** When source = 'grn', which key on PMInwardRow to copy from. */
  grnField?: keyof PMInwardRow
  required?: boolean
  /** Render full-width on the form (spans 2 grid columns). */
  fullWidth?: boolean
}

