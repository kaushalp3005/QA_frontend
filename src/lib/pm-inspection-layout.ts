// ── Form data shape (what the create page builds & POSTs) ─────────────
export interface ParamObservation {
  observation: string
  status: string
}

export interface PMInspectionFormData {
  // Source GRN reference (frozen at creation time)
  company: string
  transaction_no: string
  grn_number: string

  // Header
  sr_no: string
  received_date: string
  inspection_date: string
  material_description: string
  challan_no: string
  invoice_no: string
  supplier_name: string
  vehicle_no: string
  coa_received: 'yes' | 'no' | ''
  quantity: string

  // Visual Verification — keyed by ParamRowDef.key
  visual_verification: Record<string, ParamObservation>
  // Inspection Parameters — keyed by ParamRowDef.key
  inspection_parameters: Record<string, ParamObservation>

  // Footer
  remarks: string
  done_by: string
  verified_by: string
}

// ── Helpers ───────────────────────────────────────────────────────────
export function emptyParamMap(defs: ParamRowDef[]): Record<string, ParamObservation> {
  return Object.fromEntries(defs.map((d) => [d.key, { observation: '', status: '' }]))
}

export function buildEmptyForm(grn?: PMInwardRow | null): PMInspectionFormData {
  const today = new Date().toISOString().slice(0, 10)
  const f: PMInspectionFormData = {
    company: grn?.company || '',
    transaction_no: grn?.transaction_no || '',
    grn_number: grn?.grn_number || '',
    sr_no: '',
    received_date: '',
    inspection_date: today,
    material_description: '',
    challan_no: '',
    invoice_no: '',
    supplier_name: '',
    vehicle_no: '',
    coa_received: '',
    quantity: '',
    visual_verification: emptyParamMap(VISUAL_VERIFICATION_PARAMS),
    inspection_parameters: emptyParamMap(INSPECTION_PARAMS),
    remarks: '',
    done_by: '',
    verified_by: '',
  }

  if (grn) {
    for (const def of HEADER_FIELDS) {
      if (def.source !== 'grn' || !def.grnField) continue
      const raw = (grn as any)[def.grnField]
      if (raw == null) continue
      ;(f as any)[def.key] = String(raw)
    }
  }

  return f
}

